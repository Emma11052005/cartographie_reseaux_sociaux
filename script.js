let current = 0;
let selectedAnswer = null;
let results = {};

window.addEventListener("load", openQuiz);

function openQuiz() {
    const modal = document.getElementById("quizModal");
    if (modal) modal.style.display = "block";
    showQuestion();
}

function showQuestion() {
    const q = questions[current];

    document.getElementById("progress").innerText =
        `${current + 1} / ${questions.length}`;

    document.getElementById("question-container").innerText = q.q;

    document.getElementById("openAnswer").value = "";

    selectedAnswer = null;
    document.getElementById("validerBtn").disabled = true;

    renderButtons(q.type);
}

function renderButtons(type) {
    const container = document.getElementById("choice-area");

    let html = "";

    if (type === "yesno") {
        html = `
            <button onclick="selectAnswer('Oui', this)">Oui</button>
            <button onclick="selectAnswer('Non', this)">Non</button>
        `;
    }

    if (type === "social") {
        html = `
            <button onclick="selectAnswer('Lien social', this)">Renforcent les liens</button>
            <button onclick="selectAnswer('Isolement', this)">Créent de l’isolement</button>
        `;
    }

    if (type === "posneg") {
        html = `
            <button onclick="selectAnswer('Positif', this)">Positif</button>
            <button onclick="selectAnswer('Négatif', this)">Négatif</button>
        `;
    }

    container.innerHTML = html;
}
function selectAnswer(value, btn) {
    selectedAnswer = value;

    // désactive tous les boutons
    const buttons = document.querySelectorAll("#choice-area button");
    buttons.forEach(b => b.classList.remove("selected"));

    // active celui cliqué
    btn.classList.add("selected");

    document.getElementById("validerBtn").disabled = false;
}

function nextQuestion() {
    if (!selectedAnswer) return;

    const q = questions[current];

    results[q.key] = {
        answer: selectedAnswer,
        why: document.getElementById("openAnswer").value
    };

    current++;

    if (current < questions.length) {
        showQuestion();
    } else {
        finishQuiz();
    }
}

function finishQuiz() {
    document.getElementById("quizModal").innerHTML = `
        <div class="modal-content">
            <h2>Résultats</h2>
            <p>Merci pour votre participation.</p>

            <button class="cta-end" onclick="goToStudy()">
                Accéder à l’étude de la controverse
            </button>
        </div>
    `;

    localStorage.setItem("quiz_results", JSON.stringify(results));
}

function goToStudy() {
    document.getElementById("quizModal").style.display = "none";
    document.getElementById("debut").scrollIntoView({ behavior: "smooth" });
}

