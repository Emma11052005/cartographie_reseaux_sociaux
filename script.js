function drawLine(x1, y1, x2, y2, svg) {
    if (!svg) return;

    const line = document.createElementNS("http://www.w3.org/2000/svg", "line");

    line.setAttribute("x1", x1);
    line.setAttribute("y1", y1);
    line.setAttribute("x2", x2);
    line.setAttribute("y2", y2);

    line.setAttribute("stroke", "#a855f7");
    line.setAttribute("stroke-width", "2");

    svg.appendChild(line);
}

function connectNodes() {
    const svg = document.getElementById("mindmap-lines");
    const central = document.getElementById("central-node");

    if (!svg || !central) return;

    svg.innerHTML = "";

    const nodes = [
        "politique-node",
        "social-node",
        "sante-node",
        "jeunesse-node",
        "eco-node",
        "business-node"
    ];

    const c = central.getBoundingClientRect();
    const s = svg.getBoundingClientRect();

    const cx = c.left + c.width / 2 - s.left;
    const cy = c.top + c.height / 2 - s.top;

    nodes.forEach(id => {
        const node = document.getElementById(id);
        if (!node) return;

        const r = node.getBoundingClientRect();

        const nx = r.left + r.width / 2 - s.left;
        const ny = r.top + r.height / 2 - s.top;

        drawLine(cx, cy, nx, ny, svg);
    });
}

function positionNodes() {
    const container = document.querySelector(".mindmap-container");
    const central = document.getElementById("central-node");

    if (!container || !central) return;

    const w = container.clientWidth;
    const h = container.clientHeight;

    central.style.left = `${w / 2 - central.offsetWidth / 2}px`;
    central.style.top = `${h / 2 - central.offsetHeight / 2}px`;

    const nodes = [
        "politique-node",
        "social-node",
        "sante-node",
        "jeunesse-node",
        "eco-node",
        "business-node"
    ];

    const radius = Math.min(w, h) / 3;

    nodes.forEach((id, i) => {
        const node = document.getElementById(id);
        if (!node) return;

        const angle = (i / nodes.length) * 2 * Math.PI;

        node.style.left = `${w / 2 + radius * Math.cos(angle) - node.offsetWidth / 2}px`;
        node.style.top = `${h / 2 + radius * Math.sin(angle) - node.offsetHeight / 2}px`;
    });
}

function initMindmap() {
    positionNodes();
    connectNodes();
}

window.addEventListener("load", initMindmap);
window.addEventListener("resize", initMindmap);

